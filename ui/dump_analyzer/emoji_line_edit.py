"""Provides a QLineEdit with an emoji selector button."""
from PySide6.QtCore import QPoint, Qt, Signal
from PySide6.QtGui import QCursor, QFocusEvent, QResizeEvent
from PySide6.QtWidgets import (QGridLayout, QLineEdit, QTabWidget, QToolButton,
                               QVBoxLayout, QWidget)


class EmojiSelector(QWidget):
    """
    A simple dialog to select an emoji
    """
    emoji_selected = Signal(str)

    def __init__(self, *, emojis: dict[str, list[list[str]]] | None = None, parent: QWidget | None = None):
        super().__init__(parent)
        self._initial_position: QPoint | None = None
        self.setWindowTitle("Select Emoji")
        self.setWindowFlags(Qt.WindowType.Popup | Qt.WindowType.FramelessWindowHint)
        self.setAttribute(Qt.WidgetAttribute.WA_TranslucentBackground, True)
        self.setAttribute(Qt.WidgetAttribute.WA_NoSystemBackground, True)

        if emojis is None:
            emojis = default_emojis

        layout = QVBoxLayout(self)
        layout.setContentsMargins(0, 0, 0, 0)
        tab_widget = QTabWidget(self)
        layout.addWidget(tab_widget)
        for category, emoji_rows in emojis.items():
            tab = QWidget()
            grid = QGridLayout(tab)
            grid.setContentsMargins(0, 0, 0, 0)
            grid.setSpacing(0)
            for row, emoji_row in enumerate(emoji_rows):
                for col, emoji in enumerate(emoji_row):
                    button = QToolButton(self)
                    button.setText(emoji)
                    button.setCursor(Qt.CursorShape.PointingHandCursor)
                    button.setFocusPolicy(Qt.FocusPolicy.NoFocus)
                    button.setAutoRaise(True)
                    button.clicked.connect(lambda checked, e=emoji: self.emoji_selected.emit(e)) # type: ignore
                    grid.addWidget(button, row, col)
            tab_widget.addTab(tab, category)

    def show_at_cursor(self) -> None:
        self.move(QCursor.pos())
        self.show()


class EmojiLineEdit(QLineEdit):
    """
    A QLineEdit that shows a little button to add an emoji
    """

    def __init__(self, contents: str = '', parent: QWidget | None = None):
        super().__init__(contents, parent)
        self._emoji_button = QToolButton(self)
        self._emoji_button.setText("😀")
        self._emoji_button.setCursor(Qt.CursorShape.PointingHandCursor)
        self._emoji_button.setFocusPolicy(Qt.FocusPolicy.NoFocus)
        self._emoji_button.setAutoRaise(True)
        self._emoji_button.setStyleSheet("QToolButton { padding: 0px; }")
        self._emoji_button.clicked.connect(self._insert_emoji)

        button_height = self._emoji_button.sizeHint().height()
        button_width = self.fontMetrics().horizontalAdvance(self._emoji_button.text()) + 4
        self._emoji_button.setFixedSize(button_width, button_height)
        self._position_emoji_button()
        self._update_emoji_button_visibility(self.hasFocus())

        self._emoji_selector: EmojiSelector | None = None

    def focusInEvent(self, event: QFocusEvent) -> None:
        super().focusInEvent(event)
        self._update_emoji_button_visibility(True)

    def focusOutEvent(self, event: QFocusEvent) -> None:
        super().focusOutEvent(event)
        self._update_emoji_button_visibility(False)

    def resizeEvent(self, event: QResizeEvent) -> None:
        super().resizeEvent(event)
        self._position_emoji_button()

    def _position_emoji_button(self) -> None:
        frame_width = self.style().pixelMetric(self.style().PixelMetric.PM_DefaultFrameWidth)
        button_size = self._emoji_button.size()
        x = self.rect().right() - frame_width - button_size.width()
        y = (self.rect().height() - button_size.height()) // 2
        self._emoji_button.move(x, y)

    def _update_emoji_button_visibility(self, is_visible: bool) -> None:
        self._emoji_button.setVisible(is_visible)
        right_margin = self._emoji_button.width() if is_visible else 0
        self.setTextMargins(0, 0, right_margin, 0)

    def _insert_emoji(self) -> None:
        if not self._emoji_selector:
            self._emoji_selector = EmojiSelector(parent=self)
            self._emoji_selector.emoji_selected.connect(self.insert)
        self._emoji_selector.show_at_cursor()

